def table(table_arr):
    ins = '''<tr>
                  <td class="text-primary">URL</td>
                  <td class="text-primary">TEXT</td>
                  <td class="text-primary">TAGS</td>
                  <td>
                    <a href="/delete/url_base64" class="btn btn-outline-primary">Delete</a>
                  </td>
                </tr>
                '''
    #urllib.parse.quote_plus(...)

    center = ''
    for x in table_arr:
        center += ins.replace('URL',x[0]).replace('TEXT',x[1]).replace('TAGS',x[2]).replace('url_base64',base64.b64encode(x[0].encode('utf-8')).decode('utf-8'))

    f = open('design\\table.html', encoding="utf8")
    s = f.read()
    f.close()
    s = s.replace('%%insert%%',center)
    return s

