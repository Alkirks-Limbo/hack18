def search(arr):
    inc = """<ul class="breadcrumb" style="margin-bottom: 0px; margin-top: 0px;">
            <li class="breadcrumb-item">
              <a href="URL">PATH</a>
            </li>
            <li class="breadcrumb-item active">- ABOUT</li>
          </ul>
          """
    center = ''
    for x in arr:
        center += inc.replace('URL','http://127.0.0.1:5000'+x[0]).replace('PATH',x[0]).replace('ABOUT',x[1])

    f = open('design\\search.html', encoding="utf8")
    s = f.read()
    f.close()
    s = s.replace('%%insert%%',center)
    return s

